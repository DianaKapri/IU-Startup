def get_difficulty(subject: str, grade: int, fallback: int = 5) -> int:
    """Возвращает балл трудности предмета для класса grade."""
    if not subject:
        return 0
    code = normalize_subject(subject)
    if grade <= 4:
        return D_GRADES_1_4.get(code, fallback)
    if grade >= 10:
        return D_GRADES_10_11.get(code, fallback)
    table = D_GRADES_5_9.get(code)
    if not table:
        return fallback
    if grade in table:
        return table[grade]
    # Fallback: ближайший grade в таблице
    for delta in (1, 2, 3, 4):
        if grade - delta in table:
            return table[grade - delta]
        if grade + delta in table:
            return table[grade + delta]
    return fallback


def is_hard(subject: str, grade: int) -> bool:
    """Сложный предмет: difficulty >= 8 (или >= 7 для младших)."""
    threshold = 7 if grade <= 4 else 8
    return get_difficulty(subject, grade) >= threshold


# Светлые/профильные коды
LIGHT_CODES = {'фк', 'муз', 'изо', 'обж', 'техн', 'мхк', 'орксэ', 'однк', 'окрмир'}


def is_light(subject: str) -> bool:
    """Профильный «лёгкий» предмет: физкультура, музыка, ИЗО, ОБЖ, технология."""
    return normalize_subject(subject) in LIGHT_CODES
