def normalize_subject(raw: str) -> str:
    """«Математика» → «мат», «физическая культура» → «фк»."""
    if not raw:
        return ''
    s = raw.lower().replace('ё', 'е').strip()
    if s in NORM_ALIASES:
        return NORM_ALIASES[s]
    # Substring match
    for key, code in NORM_ALIASES.items():
        if key in s or s in key:
            return code
    return s[:6]


